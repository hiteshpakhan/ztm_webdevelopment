// TYPE 1: function declaration 
// syntax:  function_name(){}

function say_hello(){
    console.log("hello");
}
say_hello();

// it will give the output:-
// hello



// TYPE 2: function expression
// syntax: var variable_name = function () {}

var say_bye = function(){
    console.log("BYE");
}
say_bye();
// it will give the output:-
// BYE














